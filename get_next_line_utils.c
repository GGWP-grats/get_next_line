#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list *new_list;
	t_list *first;

	if (!lst)
		return (NULL);
	else
	{
		first = NULL;
		while (lst)
		{
			if ((new_list = ft_lstnew(f(lst->content))))
				ft_lstadd_back(&first, new_list);
			else
			{
				ft_lstclear(&first, del);
				return (NULL);
			}
			lst = lst->next;
		}
		return (first);
	}
}
